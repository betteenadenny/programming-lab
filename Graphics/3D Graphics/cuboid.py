def cuboid_area():
    l=int(input("Enter the length of cuboid:"))
    b=int(input("Enter the breadth of cuboid:"))
    h=int(input("Enter the height of cuboid:"))
    area=2*((l*b)+(b*h)+(l*h))
    print("Area of cuboid is ",area)

def cuboid_volume():
    l=int(input("Enter the length of cuboid:"))
    b=int(input("Enter the breadth of cuboid:"))
    h=int(input("Enter the height of cuboid:"))
    volume=l*b*h
    print("volume of cuboid is ",volume)
    
